# Iterável
## Iterável é um tipo de dado que nos permite iterar, ou seja, passar por cada valor dentro dele. Esse dado pode gerar valores ou já possuir valores dentro dele.

for item in range(10): # Estamos iterando (passando por cada um dos 10 itens) usando o iterável (range(10))
  print(item)

print('------------------------------------')

for letra in 'programação':
  print(letra)

print('------------------------------------')

for item in [2, 3, 4, 5]:
  print(item)
