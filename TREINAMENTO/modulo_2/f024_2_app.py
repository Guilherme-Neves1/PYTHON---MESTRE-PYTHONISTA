# Operações Matemáticas
print(10 + 6)
print(10 - 6)
print(10 * 6)
print(10 / 6)
print(10 // 6) # divisão de inteiro, quantas vezes 6 entra em 10
print(10 % 6) # módulos, restante da divisão
print(10 ** 6) # exponenciais

# Atalho para atribuição mais rápida
