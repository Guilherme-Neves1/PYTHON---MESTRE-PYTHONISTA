# Para criar strings, podemos adicionar aspas à frase

print('Codar todos os dias!')
print("Codar todos os dias!!!")

# Aspas triplas para frases com mais de uma linha
print('''Estamos codando todos os dias.
Estou aprendendo muito!''')
print("""Hoje eu estou codando.
Amanhã também.""")

# Caracteres de escape
print('Meu nome é \nGuilherme')
print('Codar todos \'os dias')

print('Arquivo localizado em \c:drive\arquivos.txt') # A \ ignora a letra 'c' e 'a'
print('Arquivo localizado em \\c:drive\\arquivos.txt')

nome = 'Carol'
print(len(nome))

# DESAFIO
